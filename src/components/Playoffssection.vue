  { id: 207, label: "above", bgName: "above_rc1" },
];

const demomanMaps = [
  { id: 3, label: "tissue", bgName: "tissue" },
  { id: 817, label: "carbon", bgName: "carbon_b1" },
  { id: 472, label: "data", bgName: "data_final" },
  { id: 815, label: "greenguy2", bgName: "greenguy2_b2" },
  { id: 807, label: "legocroc", bgName: "legocroc_rc1" },
  { id: 99, label: "nbn redo", bgName: "nbn_b4b_redo_a1" },
];

const PLAYER_DEFS = [
  { id: 10736, name: "Vice", classes: ["soldier", "demoman"] },
  { id: 357942, name: "Bunny", classes: ["soldier"] },
  { id: 602086, name: "Nikita", classes: ["soldier"] },
  { id: 579826, name: "Garf", classes: ["soldier"] },
  { id: 503501, name: "Sammy", classes: ["soldier"] },
  { id: 31339, name: "Hass", classes: ["soldier"] },
  { id: 74551, name: "Rev!4", classes: ["soldier"] },
  { id: 24856, name: "Spidda", classes: ["soldier"] },
  { id: 281915, name: "Newjuls", classes: ["demoman"] },
  { id: 243670, name: "Cander", classes: ["demoman"] },
  { id: 35454, name: "Nnameee", classes: ["demoman"] },
];

// Final standings — used to apply gold/silver/bronze borders to player chips
const PLACEMENTS = {
  soldier: {
    10736: 1, // Vice
    357942: 2, // Bunny
    602086: 3, // Nikita
  },
  demoman: {
    // empty until Demo finishes
  },
};

function placeClass(playerId, playerClass) {
  const place = PLACEMENTS[playerClass]?.[playerId];
  if (place === 1) return "place-gold";
  if (place === 2) return "place-silver";
  if (place === 3) return "place-bronze";
  return "";
}

function placeBadge(playerId, playerClass) {
  const place = PLACEMENTS[playerClass]?.[playerId];
  if (place === 1) return "🥇";
  if (place === 2) return "🥈";
  if (place === 3) return "🥉";
  return "";
}

const soldierPlayers = ref(
  PLAYER_DEFS.filter((p) => p.classes.includes("soldier")).map((p) => ({
    ...p,
    steam_avatar: null,
    country_code: null,
    country: null,
  })),
);
const demomanPlayers = ref(
  PLAYER_DEFS.filter((p) => p.classes.includes("demoman")).map((p) => ({
    ...p,
    steam_avatar: null,
    country_code: null,
    country: null,
  })),
);

async function fetchPlayer(id) {
  try {
    const res = await axios.get(`${API_BASE_URL}/players/${id}`);
    const data = res.data[0];

    const patch = (list) => {
      const idx = list.value.findIndex((p) => p.id === id);
      if (idx !== -1) {
        list.value.splice(idx, 1, {
          ...list.value[idx],
          steam_avatar: data.steam_avatar ?? null,
          country_code: data.country_code ?? null,
          country: data.country ?? null,
        });
      }
    };
    patch(soldierPlayers);
    patch(demomanPlayers);
  } catch (e) {
    console.error(`Failed to fetch player ${id}:`, e);
  }
}

onMounted(() => {
  const uniqueIds = [...new Set(PLAYER_DEFS.map((p) => p.id))];
  uniqueIds.forEach((id) => fetchPlayer(id));
});
</script>

